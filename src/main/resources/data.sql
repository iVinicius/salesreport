CREATE TABLE CLIENT_T (
CNPJ int,
PRIMARY KEY (CNPJ)
);

CREATE TABLE SELLER_T (
CPF int,
NAME varchar(255),
SALARY float,
PRIMARY KEY (CPF)
);

CREATE TABLE SALE_T (
SALE_ID int,
SELLER_NAME varchar(225),
PRIMARY KEY (SALE_ID)
);

CREATE TABLE SALE_ITEM_T (
SALE_ID int,
ITEM_ID int,
QUANTITY int,
PRICE float,
PRIMARY KEY (SALE_ID, ITEM_ID)
);